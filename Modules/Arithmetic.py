# Q1. Write a program to create a module Arithmetic having four functions named add, sub, div and mul. 
# All functions are parameterised and input are taken from user.

def add(num1, num2):
    return num1+num2

def sub(num1, num2):
    return num1 - num2

def div(num1, num2):
    return num1/num2

def mul(num1, num2):
    return num1*num2